#!/usr/bin/env python3
"""
this module provides a function to add two float numbers.
"""


def add(a: float, b: float) -> float:
    """
    Function to add two float numbers.

    Args:
        a (float): First float number.
        b (float): Second float number.

    Returns:
        float: Sum of a and b.
    """
    return a + b
